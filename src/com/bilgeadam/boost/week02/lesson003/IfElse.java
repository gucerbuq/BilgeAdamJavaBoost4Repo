package com.bilgeadam.boost.week02.lesson003;

public class IfElse {

	public static void main(String[] args) {
		
		int x,y;
		
		x=8;
		y=9;
		
		if(x<y) {
			System.out.println("x is smaller than y.");
		}else if(x==y) {
			System.out.println("x is equal to y");
		}else {
			System.out.println("x is greater than y.");
		}

	}

}
